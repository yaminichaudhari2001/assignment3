// Java Program to print the elements of an array
public class Main {
    public static void main(String[] args) {
        int[] arr = { 0, 3, 20, 25, 12,100 };
        System.out.print("Elements of given array are: ");
         for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
    }
}
}